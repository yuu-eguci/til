# フォーマット文字列リテラル
# こ、これわPHPに唯一劣ると思っていた機能じゃないか! うれしー!
a = 'わーい!'
b = 'たのしー!'
c = [a, b]
print(f'{a} {b}')          # わーい! たのしー!
print(f'{c[0]} {c[1]}')    # わーい! たのしー!

# =========================
# 数値リテラルでアンダースコア利用
# 桁数の多い数字にどうぞ
a = 1_000_000
b = a + 200_000
print(b)           # 1200000
print(f'{b:,}')    # 1,200,000

