# 型注釈
# C#とかと書き方が違くてみづれえｗ
# ただしフツーに実行してエラーが出るようなものじゃなくて、外部ツールを通して実行することでチェックする形になるらしい。
# mypyってのを試してみたが、型とか関係ないpython3.6のフォーマット文字列でパースエラーが出やがったので放置。
def foo(number: int) -> str:
    return f'受け取った引数を文字列化:{number}'
print(foo(5))
